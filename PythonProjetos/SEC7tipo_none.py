""""
Tipo none

O tipo de dado none em Python representa o tipo sem tip, ou poderia ser conhecido tambem como tipo vazio, porém
falar que é um tipo sem tipo é mais apropriado
OBS: O tipo None é SEMPRE especificado com a primeira letra maiúscula

Certo = None
Errado = none

QUANDO UTILIZAMOS None?
- Podemos utilizar o tipo None quando queremos criar uma variavel e iniciar com um tipo sem tipo antes de rceber um
  valor final

OBS: O tipo None em Python é sempre considerado False
"""
numeros = None
print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.69
print(numeros)
print(type(numeros))
